def extract_json(text):
    text = re.sub(r'```(?:json)?','',text).strip('`').strip()
    try: return json.loads(text)
    except:
        m = re.search(r'\{.*\}', text, re.DOTALL)
        if m:
            try: return json.loads(m.group())
            except: pass
    return None

