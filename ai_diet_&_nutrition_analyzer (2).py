def fig_to_b64(fig):
    buf = io.BytesIO()
    fig.savefig(buf, format='png', bbox_inches='tight', dpi=110, facecolor='white')
    buf.seek(0)
    b64 = base64.b64encode(buf.read()).decode()
    plt.close(fig)
    return f'<img src="data:image/png;base64,{b64}" style="max-width:100%;border-radius:10px;margin-top:10px;">'

def macro_pie_html(macros, title=""):
    labels,sizes,colors = [],[],[]
    cmap = {'protein':('#4CAF50','Protein'),'carb':('#2196F3','Carbs'),
            'fat':('#FF9800','Fat'),'fiber':('#9C27B0','Fiber'),'sugar':('#F44336','Sugar')}
    for key,val in macros.items():
        if isinstance(val,dict): val=val.get('consumed',0)
        clean=key.lower().replace('_g','')
        for k,(c,lbl) in cmap.items():
            if k in clean and float(val or 0)>0:
                sizes.append(float(val)); labels.append(f"{lbl}\n{val}g"); colors.append(c); break
    if not sizes: return ""
    fig,ax=plt.subplots(figsize=(5,4))
    ax.pie(sizes,labels=labels,colors=colors,autopct='%1.0f%%',startangle=90,
           wedgeprops=dict(width=0.55,edgecolor='white',linewidth=2))
    ax.text(0,0,f'{sum(sizes):.0f}g\nTotal',ha='center',va='center',fontsize=10,fontweight='bold')
    if title: ax.set_title(title,fontsize=11,fontweight='bold')
    return fig_to_b64(fig)

def bar_chart_html(macros):
    nutrients,consumed_vals,target_vals=[],[],[]
    for k,v in macros.items():
        if isinstance(v,dict):
            consumed_vals.append(float(v.get('consumed',0)))
            target_vals.append(float(v.get('target',1)))
            nutrients.append(k.replace('_g','').replace('_ml','').replace('_',' ').title())
    if not nutrients: return ""
    x=np.arange(len(nutrients)); w=0.35
    bar_colors=['#4CAF50' if c<=t else '#F44336' for c,t in zip(consumed_vals,target_vals)]
    fig,ax=plt.subplots(figsize=(8,4))
    ax.bar(x-w/2,consumed_vals,w,label='Consumed',color=bar_colors,alpha=0.85,edgecolor='white')
    ax.bar(x+w/2,target_vals,  w,label='Target',  color='#90A4AE',alpha=0.6, edgecolor='white')
    ax.set_xticks(x); ax.set_xticklabels(nutrients,rotation=20,ha='right',fontsize=10)
    ax.legend(fontsize=10); ax.grid(axis='y',alpha=0.3); ax.set_ylabel('Amount')
    ax.set_title('Macronutrients: Consumed vs Target',fontsize=12,fontweight='bold')
    fig.patch.set_facecolor('white')
    return fig_to_b64(fig)


