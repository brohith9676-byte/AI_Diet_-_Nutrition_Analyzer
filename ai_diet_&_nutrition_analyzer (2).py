def render_chat():
    with out_chat_msgs:
        clear_output()
        if not app_state["chat_history"]:
            display(HTML('<div style="text-align:center;padding:30px;color:#888">👋 Hi! I am Dr. Nutri. Ask me anything about diet and nutrition!</div>'))
            return
        bubbles = ""
        for h in app_state["chat_history"]:
            bubbles += f'<div class="chat-bubble chat-user">🧑 {h["q"]}</div>'
            bubbles += f'<div class="chat-bubble chat-ai">🥗 <b>Dr. Nutri:</b><br>{h["a"].replace(chr(10),"<br>")}</div>'
        display(HTML(APP_CSS + f'<div class="app-container">{bubbles}</div>'))

def on_chat_send(b):
    q = chat_input.value.strip()
    if not q: return
    chat_input.value = ""
    with out_chat_msgs:
        clear_output()
        display(HTML('<div class="spinner">💬 Dr. Nutri is thinking...</div>'))
    ans = chat_response(q, app_state["chat_history"], app_state["chat_profile_ctx"])
    app_state["chat_history"].append({"q":q,"a":ans})
    render_chat()

def on_chat_clear(b):
    app_state["chat_history"] = []
    render_chat()

chat_send.on_click(on_chat_send)
chat_clear.on_click(on_chat_clear)

